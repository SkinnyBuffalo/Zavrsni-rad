package com.zavrsni.evidencijastudenata.Soba;
public class Soba {
    private int idSobe;
    private int brojSobe;
    private int katSobe;
    private int brojObjekta;


    public int getIdSobe() {
        return idSobe;
    }

    public void setIdSobe(int idSobe) {
        this.idSobe = idSobe;
    }

    public int getBrojSobe() {
        return brojSobe;
    }

    public void setBrojSobe(int brojSobe) {
        this.brojSobe = brojSobe;
    }

    public int getKatSobe() {
        return katSobe;
    }

    public void setKatSobe(int katSobe) {
        this.katSobe = katSobe;
    }

    public int getBrojObjekta() {
        return brojObjekta;
    }

    public void setBrojObjekta(int brojObjekta) {
        this.brojObjekta = brojObjekta;
    }
}
